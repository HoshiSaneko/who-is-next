import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const UP_CARD_API = 'https://api.bilibili.com/x/web-interface/card';
const delay = (ms) => new Promise((resolve) => setTimeout(resolve, ms));

function extractUids() {
  const upMembersPath = path.join(__dirname, '../configs/upMembers.config.ts');
  const goddessesPath = path.join(__dirname, '../configs/goddesses.config.ts');
  const upMembersContent = fs.readFileSync(upMembersPath, 'utf8');
  const goddessesContent = fs.readFileSync(goddessesPath, 'utf8');
  const upMemberUids = [...upMembersContent.matchAll(/uid:\s*['"](\d+)['"]/g)].map((match) => match[1]);
  const goddessUids = [...goddessesContent.matchAll(/space\.bilibili\.com\/(\d+)/g)].map((match) => match[1]);

  return [...new Set([...upMemberUids, ...goddessUids])];
}

async function fetchAvatar(uid) {
  const response = await fetch(`${UP_CARD_API}?mid=${uid}`, {
    headers: {
      'User-Agent': 'Mozilla/5.0',
      Referer: 'https://www.bilibili.com/',
    },
  });

  if (!response.ok) throw new Error(`HTTP ${response.status}`);

  const payload = await response.json();
  if (payload.code !== 0 || !payload.data?.card?.face) {
    throw new Error(payload.message || 'Missing avatar URL');
  }

  return payload.data.card.face.replace(/^http:/, 'https:');
}

function writeConfig(avatars) {
  const outputPath = path.join(__dirname, '../configs/biliAvatars.config.ts');
  const mapping = [...avatars.entries()]
    .map(([uid, avatar]) => `  '${uid}': '${avatar}',`)
    .join('\n');

  fs.writeFileSync(
    outputPath,
    `/** Generated by scripts/fetch-bili-avatars.js. */\nexport const BILI_AVATAR_MAPPING: Record<string, string> = {\n${mapping}\n};\n\nexport function getBiliOfficialAvatar(uid?: string): string | null {\n  return uid ? BILI_AVATAR_MAPPING[uid]?.replace(/^http:/, 'https:') || null : null;\n}\n`,
    'utf8',
  );

  console.log(`Avatar mapping saved to ${outputPath}`);
}

async function main() {
  const uids = extractUids();
  const avatars = new Map();

  for (const [index, uid] of uids.entries()) {
    try {
      const avatar = await fetchAvatar(uid);
      avatars.set(uid, avatar);
      console.log(`[${index + 1}/${uids.length}] ${uid}: OK`);
    } catch (error) {
      console.error(`[${index + 1}/${uids.length}] ${uid}: ${error.message}`);
    }

    if (index < uids.length - 1) await delay(500);
  }

  if (avatars.size !== uids.length) {
    throw new Error(`Only fetched ${avatars.size} of ${uids.length} avatar URLs; config was not written.`);
  }

  writeConfig(avatars);
}

main().catch((error) => {
  console.error(error.message);
  process.exitCode = 1;
});
